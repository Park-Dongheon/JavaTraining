package chapter02;

public class Test6 {
	public static void main(String[] args) {
		int a = 10 + 2;				// +, 더하기 연산자
		int b = 10 - 2;				// -, 빼기 연선자
		int c = 10 * 2;				// *, 곱하기 연산자
		int d = 10 / 2;				// /, 나누기 연산자, 변수 d에 몫값만 저장
		int e = 10 % 2;				// %, 나머지 연산자
		
		System.out.println(a);		// 12
		System.out.println(b);		// 8
		System.out.println(c);		// 20
		System.out.println(d);		// 5
		System.out.println(e);		// 0
	}
}
